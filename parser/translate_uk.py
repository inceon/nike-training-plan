def translate_value(value: Any) -> Any:
    if isinstance(value, str):
        return translate_string(value)
    if isinstance(value, list):
        return [translate_value(item) for item in value]
    if isinstance(value, dict):
        translated = {key: translate_value(item) for key, item in value.items()}
        if "language" in translated and translated["language"] == "ru":
            translated["language"] = "uk"
        return translated
    return value


def translate_string(text: str) -> str:
    translated = text
    for pattern, replacement in PHRASE_REPLACEMENTS:
        translated = pattern.sub(replacement, translated)
    for pattern, replacement in REGEX_REPLACEMENTS:
        translated = pattern.sub(replacement, translated)
    for pattern, replacement in WORD_REPLACEMENTS:
        translated = pattern.sub(replacement, translated)
    translated = translated.replace("в этом", "у цьому")
    translated = translated.replace("в другие дни", "в інші дні")
    translated = translated.replace("в дни", "у дні")
    translated = translated.replace("в приложении", "у застосунку")
    translated = translated.replace("Теперь ", "Тепер ")
    translated = translated.replace("Твой ", "Твій ")
    translated = translated.replace("твой ", "твій ")
    translated = translated.replace("себе", "собі")
    translated = translated.replace("своим", "своїм")
    translated = translated.replace("свою", "свою")
    translated = translated.replace("свои", "свої")
    translated = translated.replace("лучше", "краще")
    return translated


def translate_output_file(source_path: str | Path, destination_path: str | Path) -> None:
    source_path = Path(source_path)
    destination_path = Path(destination_path)
    data = json.loads(source_path.read_text(encoding="utf-8"))
    translated = translate_value(data)
    destination_path.write_text(
        json.dumps(translated, ensure_ascii=False, separators=(",", ":")),
        encoding="utf-8",
    )


