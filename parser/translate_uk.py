def _ws_pattern(text: str) -> re.Pattern[str]:
    escaped = re.escape(text.strip())
    escaped = escaped.replace(r"\ ", r"\s+")
    escaped = escaped.replace(r"\\\n", r"\s+")
    return re.compile(escaped, re.MULTILINE)


